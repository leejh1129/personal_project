package chap06;

public class Printer {

	void println(int println) {
		System.out.println(println);
	}
	
	void println(boolean println) {
		System.out.println(println);
	}
	
	void println(double println) {
		System.out.println(println);
	}
	
	void println(String println) {
		System.out.println(println);
	}
	
}
